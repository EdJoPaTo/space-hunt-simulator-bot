export function replaceTinyLetters(text: string): string {
	return text
		.replace(/⁰/g, '0')
		.replace(/¹/g, '1')
		.replace(/²/g, '2')
		.replace(/³/g, '3')
		.replace(/⁴/g, '4')
		.replace(/⁵/g, '5')
		.replace(/⁶/g, '6')
		.replace(/⁷/g, '7')
		.replace(/⁸/g, '8')
		.replace(/⁹/g, '9')
		.replace(/ᵃ/g, 'a')
		.replace(/ᵇ/g, 'b')
		.replace(/ᶜ/g, 'c')
		.replace(/ᵈ/g, 'd')
		.replace(/ᵉ/g, 'e')
		.replace(/ᶠ/g, 'f')
		.replace(/ᵍ/g, 'g')
		.replace(/ᶦ/g, 'i')
		.replace(/ˡ/g, 'l')
		.replace(/ᵐ/g, 'm')
		.replace(/ⁿ/g, 'n')
		.replace(/ᵒ/g, 'o')
		.replace(/ᵖ/g, 'p')
		.replace(/ʳ/g, 'r')
		.replace(/ˢ/g, 's')
		.replace(/ᵗ/g, 't')
		.replace(/ᵘ/g, 'u')
		.replace(/ʷ/g, 'w')
		.replace(/ʸ/g, 'y')
		.replace(/ᐟ/g, '/')
}
